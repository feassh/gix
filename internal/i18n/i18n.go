package i18n

import (
	"fmt"
)

type Locale string

const (
	English           Locale = "en"
	SimplifiedChinese Locale = "zh-CN"
	overrideEnvName          = "GIX_LANG"
)

type Catalog struct {
	locale Locale
}

func NewCatalog(locale Locale) Catalog {
	if match := parseLocale(string(locale)); match.supported {
		return Catalog{locale: match.locale}
	}
	return Catalog{locale: English}
}

func (c Catalog) Locale() Locale {
	return c.locale
}

func (c Catalog) S(key string, args ...any) string {
	template := englishMessages[key]
	if c.locale == SimplifiedChinese {
		if value, ok := simplifiedChineseMessages[key]; ok {
			template = value
		}
	}
	if template == "" {
		template = key
	}
	if len(args) == 0 {
		return template
	}
	return fmt.Sprintf(template, args...)
}

